# Swap Value Using Third Variable By Zayan

print("Swap Value Using Third Variable")

num1 = int(input("Enter A Number\n:"))
num2 = int(input("Enter Another Number\n:"))

print(f"num1 : {num1}")
print(f"num2 : {num2}")

swap = num2
num2 = num1
num1 = swap

print(f"num1 : {num1}")
print(f"num2 : {num2}")


